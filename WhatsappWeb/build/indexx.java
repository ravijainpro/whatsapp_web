    private static final int STATUS_COL = 1;
    
    private static JTable getNewRenderedTable(final JTable table) {
        table.setDefaultRenderer(Object.class, new DefaultTableCellRenderer(){
            @Override
            public Component getTableCellRendererComponent(JTable table,
                    Object value, boolean isSelected, boolean hasFocus, int row, int col) {
                super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, col);
                String status = (String)table.getModel().getValueAt(row, STATUS_COL);
                if ("SENT".equals(status)) {
                    setBackground(new java.awt.Color(9, 127, 220));
                    setForeground(Color.WHITE);
                    setFont(new java.awt.Font("Comic Sans MS", 0, 16));
                    setHorizontalAlignment(DefaultTableCellRenderer.RIGHT);
                }
                else if ("RECEIVED".equals(status)) {
                    setBackground(Color.BLACK);
                    setFont(new java.awt.Font("Comic Sans MS", 0, 16));
                    setForeground(Color.WHITE);
                    setHorizontalAlignment(DefaultTableCellRenderer.LEFT);
                }
                else {
                    setFont(new java.awt.Font("Comic Sans MS", 0, 11));
                    setBackground(table.getBackground());
                    setForeground(table.getForeground());
                }       
                return this;
            }   
        });
        return table;
    }
    

